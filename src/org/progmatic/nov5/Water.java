package org.progmatic.nov5;

public class Water {
    private double portion = 0.5;
    private int price = 490;
    public Water() {
    }

    public double getPortion() {
        return portion;
    }

    public void setPortion(double portion) {
        this.portion = portion;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
